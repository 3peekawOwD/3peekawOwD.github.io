def sl(a, b)

    return a + b

end

def umn(c, d)

    return c * d

end

def vch(e, f)

    return e - f

end


def del(g, h)

    return g / h

end

def mod(i, j)

    return i % j

end

def fct(n)

  (1..n).inject(:*) || 1

end

def randon(n)

    return rand(n)

end

def mathsqrt(z)

    return Math.sqrt(z)

end
